#include "simCommandService.h"
#include "sim.h"

SimCommandService::SimCommandService() {
    addCommand(Command("/simStart", "Start the Sim", SimCommand::StartSim, 1,
        [this](String args) {
        return String(Sim::getInstance().start());
    }));

    addCommand(Command("/simStop", "Stop the Sim", SimCommand::StopSim, 1,
        [this](String args) {
        return String(Sim::getInstance().stop());
    }));
}